// WHILE LOOP

// print 1 to n
let i = 1 ;
let n = 9 ;

while(i<n){
    console.log(`i = ${i}`) ;
    i += 1 ;
}



// do-while loop : condition checked at last therefore loop runs atleast one time

let j = 20 ;
do{
    console.log("hello") ;
    j++ ;
}while(j< 10) ;

// print 1 to 5

let k = 1 ;
do{
    console.log(`k = ${k}`) ;
    k++ ;
}while(k<=5) ;